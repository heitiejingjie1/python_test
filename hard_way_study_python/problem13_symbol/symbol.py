class KeyWord:
    def __init__(self):
        pass

    def logic(self):
        """and: 逻辑与"""
        print(True and False)
        """or: 逻辑或"""
        print(True or False)
        """not: 逻辑非"""
        print(not True)
        """in: 判断X是否在Y中"""
        print("i" in "heitiejingjie")
        """is: 判断是否一致"""
        print("a" is "b")

    def display(self):
        self.logic()
